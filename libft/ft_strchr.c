char	*ft_strchr(const char *s, int c)
{
	char	tf;
	int		i;

	tf = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == tf)
			return ((char *)(&s[i]));
		i++;
	}
	if (tf == 0)
		return ((char *)(&s[i]));
	return (0);
}
